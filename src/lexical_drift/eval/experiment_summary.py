from __future__ import annotations

import json
from pathlib import Path
from typing import Any

import numpy as np
import yaml


def _load_json(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as f:
        payload = json.load(f)
    if not isinstance(payload, dict):
        raise ValueError(f"Expected JSON object in {path}")
    return payload


def _fmt_metric_cell(stats: object) -> str:
    if not isinstance(stats, dict):
        return "na"
    mean = stats.get("mean")
    std = stats.get("std")
    if mean is None or std is None:
        return "na"
    return f"{float(mean):.4f}±{float(std):.4f}"


def _safe_float(value: object) -> float | None:
    if value is None:
        return None
    try:
        cast = float(value)
    except (TypeError, ValueError):
        return None
    if not np.isfinite(cast):
        return None
    return cast


def _latest_match(root: Path, pattern: str) -> Path | None:
    matches = [path for path in root.glob(pattern) if path.is_file()]
    if not matches:
        return None
    matches.sort(key=lambda p: p.stat().st_mtime_ns)
    return matches[-1]


def _pick_sweep_summary(artifact_root: Path) -> Path | None:
    preferred = artifact_root / "e2e_sweep_runs" / "e2e_sweep_summary.json"
    if preferred.exists():
        return preferred
    return _latest_match(artifact_root, "**/e2e_sweep_summary.json")


def _pick_time_ablation_summary(artifact_root: Path) -> Path | None:
    preferred = artifact_root / "ablation_time_embeddings" / "ablation_summary.json"
    if preferred.exists():
        return preferred
    return _latest_match(artifact_root, "**/ablation_time_embeddings/ablation_summary.json")


def _pick_loss_summary(artifact_root: Path) -> Path | None:
    preferred = artifact_root / "ablation_loss" / "summary.json"
    if preferred.exists():
        return preferred
    return _latest_match(artifact_root, "**/ablation_loss/summary.json")


def _sort_loss_rows(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    def sort_key(row: dict[str, Any]) -> tuple[float, float]:
        f1 = _safe_float(row.get("f1_mean"))
        pr_auc = _safe_float(row.get("pr_auc_mean"))
        return (
            float("-inf") if f1 is None else f1,
            float("-inf") if pr_auc is None else pr_auc,
        )

    return sorted(rows, key=sort_key, reverse=True)


def _extract_best_row(loss_summary: dict[str, Any]) -> dict[str, Any]:
    best = loss_summary.get("best_configuration")
    if isinstance(best, dict):
        return dict(best)

    rows_raw = loss_summary.get("rows")
    rows: list[dict[str, Any]] = []
    if isinstance(rows_raw, list):
        for entry in rows_raw:
            if isinstance(entry, dict):
                rows.append(dict(entry))
    if not rows:
        raise ValueError("Loss summary does not contain any rows or best_configuration")
    return _sort_loss_rows(rows)[0]


def _load_yaml(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as f:
        data = yaml.safe_load(f) or {}
    if not isinstance(data, dict):
        raise ValueError(f"Expected YAML object in {path}")
    return data


def _write_yaml_with_comments(
    *,
    path: Path,
    payload: dict[str, Any],
    comments: list[str],
) -> None:
    prefix = "\n".join(f"# {line}" for line in comments)
    content = yaml.safe_dump(payload, sort_keys=False)
    text = f"{prefix}\n{content}" if prefix else content
    path.write_text(text, encoding="utf-8")


def _write_best_configs(
    *,
    loss_summary_path: Path,
    loss_summary: dict[str, Any],
    config_dir: Path,
) -> dict[str, str]:
    best = _extract_best_row(loss_summary)
    loss_type = str(best.get("loss_type", best.get("loss_label", "bce")))
    pos_weight = best.get("pos_weight")
    focal_gamma = _safe_float(best.get("focal_gamma"))
    f1_mean = _safe_float(best.get("f1_mean"))
    pr_auc_mean = _safe_float(best.get("pr_auc_mean"))

    train_template_path = config_dir / "train_e2e_temporal.yaml"
    eval_template_path = config_dir / "eval_e2e_temporal_calib.yaml"
    train_payload = _load_yaml(train_template_path)
    eval_payload = _load_yaml(eval_template_path)

    train_payload["loss_type"] = loss_type
    train_payload["pos_weight"] = None if pos_weight is None else float(pos_weight)
    train_payload["focal_gamma"] = 2.0 if focal_gamma is None else float(focal_gamma)

    train_best_path = config_dir / "train_e2e_best.yaml"
    eval_best_path = config_dir / "eval_e2e_best.yaml"
    promoted_dir = config_dir / "best"
    promoted_dir.mkdir(parents=True, exist_ok=True)
    promoted_train_path = promoted_dir / "train_e2e_best.yaml"
    promoted_eval_path = promoted_dir / "eval_e2e_best.yaml"

    _write_yaml_with_comments(
        path=train_best_path,
        payload=train_payload,
        comments=[
            "Auto-generated by lexdrift summarize-experiments.",
            (f"Selected from {loss_summary_path} using highest f1_mean (tie-break pr_auc_mean)."),
            (
                f"Best: loss_type={loss_type}, "
                f"pos_weight={train_payload['pos_weight']}, "
                f"focal_gamma={train_payload['focal_gamma']}, "
                f"f1_mean={f1_mean if f1_mean is not None else 'na'}, "
                f"pr_auc_mean={pr_auc_mean if pr_auc_mean is not None else 'na'}."
            ),
        ],
    )
    _write_yaml_with_comments(
        path=eval_best_path,
        payload=eval_payload,
        comments=[
            "Auto-generated by lexdrift summarize-experiments.",
            "Paired evaluation config for configs/train_e2e_best.yaml.",
            (
                "Chosen from the same loss-ablation winner to keep train/eval "
                "reporting reproducible."
            ),
        ],
    )
    _write_yaml_with_comments(
        path=promoted_train_path,
        payload=train_payload,
        comments=[
            "Promoted best config from configs/train_e2e_best.yaml.",
            "Generated by lexdrift summarize-experiments.",
        ],
    )
    _write_yaml_with_comments(
        path=promoted_eval_path,
        payload=eval_payload,
        comments=[
            "Promoted best config from configs/eval_e2e_best.yaml.",
            "Generated by lexdrift summarize-experiments.",
        ],
    )
    return {
        "train_best_config_path": str(train_best_path),
        "eval_best_config_path": str(eval_best_path),
        "promoted_train_best_config_path": str(promoted_train_path),
        "promoted_eval_best_config_path": str(promoted_eval_path),
    }


def run_summarize_experiments(
    *,
    artifact_root: str | Path = "artifacts/experiment_runs",
    config_dir: str | Path = "configs",
) -> dict[str, str]:
    root = Path(artifact_root)
    root.mkdir(parents=True, exist_ok=True)
    config_path = Path(config_dir)

    sweep_summary_path = _pick_sweep_summary(root)
    time_summary_path = _pick_time_ablation_summary(root)
    loss_summary_path = _pick_loss_summary(root)

    sweep_summary = _load_json(sweep_summary_path) if sweep_summary_path is not None else {}
    time_summary = _load_json(time_summary_path) if time_summary_path is not None else {}
    loss_summary = _load_json(loss_summary_path) if loss_summary_path is not None else {}

    lines: list[str] = [
        "# lexical-drift Experiment Summary",
        "",
        f"- artifact_root: `{root}`",
        "",
        "## Baseline Sweep (E2E)",
        "",
    ]

    per_metric = sweep_summary.get("per_metric") if isinstance(sweep_summary, dict) else None
    if isinstance(per_metric, dict):
        lines.extend(
            [
                "| metric | mean±std |",
                "|---|---:|",
            ]
        )
        for metric in (
            "f1",
            "pr_auc",
            "roc_auc",
            "balanced_accuracy",
            "brier_score",
            "ece",
            "chosen_threshold",
        ):
            lines.append(f"| {metric} | {_fmt_metric_cell(per_metric.get(metric))} |")
    else:
        lines.append("No sweep summary found.")

    sweep_dir = sweep_summary_path.parent if sweep_summary_path is not None else None
    if sweep_dir is not None:
        lines.extend(
            [
                "",
                f"- Summary JSON: `{sweep_summary_path}`",
                f"- Records CSV: `{sweep_dir / 'e2e_sweep_records.csv'}`",
                f"- Threshold stability: `{sweep_dir / 'threshold_stability.json'}`",
            ]
        )

    lines.extend(["", "## Time-Embedding Ablation", ""])
    if isinstance(time_summary, dict) and time_summary:
        delta_stats = time_summary.get("delta_f1_stats")
        t_test = time_summary.get("paired_t_test")
        ci = time_summary.get("bootstrap_ci_95")
        lines.extend(
            [
                "| metric | value |",
                "|---|---:|",
                f"| delta_f1 mean±std | {_fmt_metric_cell(delta_stats)} |",
                (
                    "| paired t-test | "
                    f"t={_safe_float(t_test.get('t_stat') if isinstance(t_test, dict) else None)} "
                    f"p={_safe_float(t_test.get('p_value') if isinstance(t_test, dict) else None)} "
                    f"n={int(t_test.get('n', 0)) if isinstance(t_test, dict) else 0} |"
                ),
                (
                    "| bootstrap 95% CI | "
                    f"mean={_safe_float(ci.get('mean') if isinstance(ci, dict) else None)} "
                    f"low={_safe_float(ci.get('low') if isinstance(ci, dict) else None)} "
                    f"high={_safe_float(ci.get('high') if isinstance(ci, dict) else None)} |"
                ),
            ]
        )
        artifact_paths = time_summary.get("artifact_paths")
        if isinstance(artifact_paths, dict):
            lines.extend(
                [
                    "",
                    f"- Summary JSON: `{time_summary_path}`",
                    f"- Deltas CSV: `{artifact_paths.get('deltas_csv_path', 'na')}`",
                    f"- Plot: `{artifact_paths.get('ablation_delta_plot_path', 'na')}`",
                ]
            )
    else:
        lines.append("No time-embedding ablation summary found.")

    lines.extend(["", "## Loss Ablation Top Configurations", ""])
    rows_raw = loss_summary.get("rows") if isinstance(loss_summary, dict) else None
    top_rows: list[dict[str, Any]] = []
    if isinstance(rows_raw, list):
        parsed_rows = [dict(entry) for entry in rows_raw if isinstance(entry, dict)]
        top_rows = _sort_loss_rows(parsed_rows)[:5]
    if top_rows:
        lines.extend(
            [
                "| rank | loss_type | pos_weight | focal_gamma | f1_mean | pr_auc_mean |",
                "|---:|---|---:|---:|---:|---:|",
            ]
        )
        for idx, row in enumerate(top_rows, start=1):
            lines.append(
                "| "
                f"{idx} | "
                f"{row.get('loss_type', row.get('loss_label', 'na'))} | "
                f"{row.get('pos_weight', 'na')} | "
                f"{row.get('focal_gamma', 'na')} | "
                f"{_safe_float(row.get('f1_mean'))} | "
                f"{_safe_float(row.get('pr_auc_mean'))} |"
            )
        lines.extend(["", f"- Summary JSON: `{loss_summary_path}`"])
        csv_path = loss_summary.get("loss_grid_results_csv")
        if csv_path is not None:
            lines.append(f"- Grid CSV: `{csv_path}`")
    else:
        lines.append("No loss ablation summary found.")

    best_config_paths: dict[str, str] = {}
    if loss_summary_path is not None and isinstance(loss_summary, dict) and loss_summary:
        best_config_paths = _write_best_configs(
            loss_summary_path=loss_summary_path,
            loss_summary=loss_summary,
            config_dir=config_path,
        )
        lines.extend(
            [
                "",
                "## Generated Best Configs",
                "",
                f"- Train config: `{best_config_paths['train_best_config_path']}`",
                f"- Eval config: `{best_config_paths['eval_best_config_path']}`",
                (
                    "- Promoted train config: "
                    f"`{best_config_paths['promoted_train_best_config_path']}`"
                ),
                f"- Promoted eval config: `{best_config_paths['promoted_eval_best_config_path']}`",
            ]
        )

    output_path = root / "EXPERIMENT_SUMMARY.md"
    output_path.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")

    result = {
        "summary_markdown_path": str(output_path),
        "artifact_root": str(root),
        "sweep_summary_path": str(sweep_summary_path) if sweep_summary_path is not None else "",
        "time_ablation_summary_path": (
            str(time_summary_path) if time_summary_path is not None else ""
        ),
        "loss_ablation_summary_path": (
            str(loss_summary_path) if loss_summary_path is not None else ""
        ),
    }
    result.update(best_config_paths)
    return result
